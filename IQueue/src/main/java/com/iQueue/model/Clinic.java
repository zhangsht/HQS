package com.iQueue.model;

public class Clinic {
	private String cId;
	private String name;
	private String oId;
	
	public String getOId() {
		return oId;
	}
	public void setOId(String oId) {
		this.oId = oId;
	}
	public String getCId() {
		return cId;
	}
	public void setCId(String cid) {
		this.cId = cid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
}
